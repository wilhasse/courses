
/*
The "strict equals" (===) and "strict not equals" (!==) compare both the value AND the type. 
The "loose equals" (==) and "loose not equals" (!=) just compare the value.
Example
5 === 6 // evaluates to false
5 !== 6 // evaluates to true

5 == 6 // evaluates to false
5 == '5' // evaluates to true
*/

// all true
console.log(5 < 6)
console.log(5 !== 6)
console.log(5 == '5')
console.log(6 >= 6)

